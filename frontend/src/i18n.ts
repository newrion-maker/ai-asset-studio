const dicts: Record<Language, Dict> = { en, ko };

export const translate = (language: Language, key: string): string => dicts[language][key] ?? dicts.en[key] ?? key;

export const useT = (): ((key: string) => string) => {
  const language = useAppStore((state) => state.language);
  return (key: string) => translate(language, key);
};
